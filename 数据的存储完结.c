int main(){
    int n = 9;
    //0 00000000 00000000000000000001001 -- 补码
    //s  e       m
    float *pFloat = (float *)&n;
    printf("n=%d\n", n);
    printf("*pFloat=%f\n", *pFloat);//(-1)^0*0.00000000000000000001001*2^-126无限接近于0
    *pFloat = 9.0;
    //1001.0
    //1.001*2^3
    //(-1)^0*1.001*2^3
    //0 100000010 0010000000000
    printf("n=%d\n", n);
    printf("*pFloat=%f\n", *pFloat);
    return 0;
}
